#TIPOS DE DADOS
## Numéricos, texto, booleanos (lógico)

#Linguagens estaticamente tipadas:
## É preciso declarar o tipo de dados antes de usar
###EX: Java, C, C++, Go, Rust

#Linguagens dinamicamente tipadas:
## Não é preciso declarar o tipo de dados antes de usar
###EX: Python, Ruby, JavaScript e PHP

#Existem linguagens fortemente tipadas e fracamente tipadas.
## JavaScript por exemplo é fracamente tipada, sendo possível misturar tipos de dados.
## O Python por sua vez, é uma linguaguem fortemente tipada, que já não permite a mistura de dados.
