print('Bem vindo ao nosso jogo de MADLIB!!')
profissao = input('Digite uma profissão: ')
nome = input('Digite um nome: ')
cidade = input('Digite uma cidade: ')
criatura = input('Digite uma criatura qualquer: ')
objeto = input('Digite um Objeto muito valioso: ')
local = input('Digite um local muito famoso: ')
cor = input('Digite uma cor: ')
gadget = input('Digite um objeto eletrônico portátil: ')
transporte = input('Digite um meio de transporte: ')
inimigo = input('Digite um plural de inimigo: ')
arma = input('Digite uma arma estranha: ')
